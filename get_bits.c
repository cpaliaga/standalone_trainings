#include <stdio.h>

int main()
{
	unsigned char c = '*';
	int bits[8]={1,2,4,8,16,32,64,128};
	int i = 0;
	while (i < 8)
	{
		if ((c & bits[i]) != 0)
			printf("1 - ");
		else
			printf("%d - ", (c & bits[i]));
		i++;
	}
	return (0);
}
